package com.koreait.first;

public class Operator5 {

	public static void main(String[] args) {
		boolean res1 = 10 >= 10;
		System.out.println(res1);
		
		boolean res2 = 10 <= 10;
		System.out.println(res2);
		System.out.println(!res2);
		
		int num1 = 10;
		int num2 = 20;
		System.out.println(num1 == num2);
		System.out.println(num1 != num2);
		System.out.println(!(num1 == num2));
	}

}
