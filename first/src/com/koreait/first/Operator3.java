package com.koreait.first;

public class Operator3 {
	public static void main(String[] args) {
		int num = 5;
		num++;
		num++;
		++num;
		System.out.println(++num);
		System.out.println(num);
		
		System.out.println();
		int num2 = 5;		
		System.out.println(5 + --num2);
		System.out.println(num2);
	}
}






