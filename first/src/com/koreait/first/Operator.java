package com.koreait.first;

public class Operator {
	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 20;
		int sum = num1 + num2;
		//int sum = 10 + 20;
		System.out.println(sum);
		
		int minus = num1 - num2;
		System.out.println(minus);
		
		int multi = num1 * num2;
		System.out.println(multi);
		
		int div = num2 / num1;
		System.out.println(div);
		
		int mod = 7 % 3;
		System.out.println(mod);
	}
}
